#!/usr/bin/env node
/**
 * Write .env.staging from .staging-database-url + HTTPS tunnel URLs.
 * Usage: node tools/bootstrap-env-staging.mjs --api-url=https://... --oidc-url=https://...
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { randomBytes } from "node:crypto";

const backendRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const dbUrlFile = path.join(backendRoot, ".staging-database-url");
const outPath = path.join(backendRoot, ".env.staging");

function arg(name) {
  const prefix = `--${name}=`;
  const hit = process.argv.find((a) => a.startsWith(prefix));
  return hit ? hit.slice(prefix.length) : "";
}

if (!existsSync(dbUrlFile)) {
  console.error("Missing .staging-database-url — run tools/probe-staging-db.mjs first");
  process.exit(1);
}

const databaseUrl = readFileSync(dbUrlFile, "utf8").trim().split(/\r?\n/)[0];
const apiUrl = (arg("api-url") || "").replace(/\/$/, "");
const oidcUrl = (arg("oidc-url") || "").replace(/\/$/, "");
if (!apiUrl.startsWith("https://") || !oidcUrl.startsWith("https://")) {
  console.error("Need --api-url=https://... and --oidc-url=https://...");
  process.exit(1);
}

const clientId = "web-admin-staging";
const clientSecret = randomBytes(24).toString("base64url");
const lines = [
  "# Generated by tools/bootstrap-env-staging.mjs — DO NOT COMMIT",
  "NODE_ENV=production",
  "SERVICE_NAME=api",
  "PORT=3000",
  "LOG_LEVEL=info",
  `DATABASE_URL=${databaseUrl}`,
  "WALKING_SKELETON_ENABLED=false",
  "OIDC_ENABLED=true",
  `OIDC_ISSUER=${oidcUrl}/`,
  `OIDC_CLIENT_ID=${clientId}`,
  `OIDC_CLIENT_SECRET=${clientSecret}`,
  `OIDC_REDIRECT_URI=${apiUrl}/api/v1/auth/oidc/callback`,
  "OIDC_SCOPES=openid profile email",
  `OIDC_AUTHORIZATION_ENDPOINT=${oidcUrl}/authorize`,
  `OIDC_TOKEN_ENDPOINT=${oidcUrl}/token`,
  "SESSION_COOKIE_NAME=ais_session",
  "SESSION_COOKIE_SECURE=true",
  "SESSION_ABSOLUTE_TTL_HOURS=12",
  "REFRESH_TTL_DAYS=30",
  "JWT_ENABLED=false",
  `STAGING_API_BASE_URL=${apiUrl}`,
];

writeFileSync(outPath, lines.join("\n") + "\n", { mode: 0o600 });
writeFileSync(path.join(backendRoot, ".staging-oidc-secret"), clientSecret, { mode: 0o600 });
console.log("Wrote .env.staging (secrets not printed)");
console.log("API=" + apiUrl);
console.log("OIDC=" + oidcUrl);
