#!/usr/bin/env node
/**
 * Interactive helper — builds backend/.env.staging from prompts (never logs secrets).
 * Usage: node tools/write-env-staging.mjs
 * See docs/release/A1-SECRETS-FILL-GUIDE.md
 */
import { createInterface } from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";
import { writeFileSync, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const backendRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outPath = path.join(backendRoot, ".env.staging");

if (existsSync(outPath) && process.env.FORCE_ENV_STAGING !== "1") {
  console.error(`Refusing to overwrite existing ${outPath}. Set FORCE_ENV_STAGING=1 to replace.`);
  process.exit(1);
}

const rl = createInterface({ input, output });
const ask = async (label) => (await rl.question(label)).trim();

console.log("A1 secrets → .env.staging (values stay on this machine only)\n");

const databaseUrl = await ask("DATABASE_URL (postgres://...@db.lrcsbrmqlyvkxxspbezi.supabase.co:5432/postgres): ");
const oidcIssuer = await ask("OIDC_ISSUER (https://<tenant>.auth0.com/): ");
const oidcClientId = await ask("OIDC_CLIENT_ID: ");
const oidcClientSecret = await ask("OIDC_CLIENT_SECRET: ");
const oidcRedirect = await ask("OIDC_REDIRECT_URI (https://<web-admin>/api/auth/oidc/callback): ");
const apiUrl = await ask("STAGING_API_BASE_URL (https://… optional, Enter to skip): ");
const webAdmin = await ask("STAGING_WEB_ADMIN_URL (https://… optional): ");
const superAdmin = await ask("STAGING_SUPER_ADMIN_URL (https://… optional): ");

rl.close();

const required = {
  DATABASE_URL: databaseUrl,
  OIDC_ISSUER: oidcIssuer,
  OIDC_CLIENT_ID: oidcClientId,
  OIDC_CLIENT_SECRET: oidcClientSecret,
  OIDC_REDIRECT_URI: oidcRedirect,
};

for (const [k, v] of Object.entries(required)) {
  if (!v || /REPLACE_|PENDING|change-me/i.test(v)) {
    console.error(`Invalid or empty ${k}`);
    process.exit(1);
  }
}
if (!required.OIDC_REDIRECT_URI.startsWith("https://")) {
  console.error("OIDC_REDIRECT_URI must be https://");
  process.exit(1);
}
if (/localhost|127\.0\.0\.1/i.test(required.DATABASE_URL)) {
  console.error("DATABASE_URL must be managed staging, not localhost");
  process.exit(1);
}

const lines = [
  "# Generated by tools/write-env-staging.mjs — DO NOT COMMIT",
  "NODE_ENV=production",
  "SERVICE_NAME=api",
  "PORT=3000",
  "LOG_LEVEL=info",
  `DATABASE_URL=${required.DATABASE_URL}`,
  "WALKING_SKELETON_ENABLED=false",
  "OIDC_ENABLED=true",
  `OIDC_ISSUER=${required.OIDC_ISSUER}`,
  `OIDC_CLIENT_ID=${required.OIDC_CLIENT_ID}`,
  `OIDC_CLIENT_SECRET=${required.OIDC_CLIENT_SECRET}`,
  `OIDC_REDIRECT_URI=${required.OIDC_REDIRECT_URI}`,
  "OIDC_SCOPES=openid profile email",
  "SESSION_COOKIE_NAME=ais_session",
  "SESSION_COOKIE_SECURE=true",
  "SESSION_ABSOLUTE_TTL_HOURS=12",
  "REFRESH_TTL_DAYS=30",
  "JWT_ENABLED=false",
];
if (apiUrl) lines.push(`STAGING_API_BASE_URL=${apiUrl}`);
if (webAdmin) lines.push(`STAGING_WEB_ADMIN_URL=${webAdmin}`);
if (superAdmin) lines.push(`STAGING_SUPER_ADMIN_URL=${superAdmin}`);

writeFileSync(outPath, lines.join("\n") + "\n", { mode: 0o600 });
console.log(`Wrote ${outPath} (${lines.length} lines). Run: node tools/preflight-staging-env.mjs`);
